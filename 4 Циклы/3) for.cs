// 2) условие можно определить в теле цикла (обязателен break)
// 3) действие с переменной при каждой новой итерации можно определить в теле цикла
// При определении чего-то из шапки в тело цикла, в шапке будет пустота с ;
// 1, 2, 3 может быть и в шапке и вне 

break - точка выхода из цикла 
continue - переход к следующей итерации цикла
Любые переменные которые создаются в блоке тела цикла будут удалены
