// Цикл где переменная создается и удаляется
for (int i = 0; i < 5; i += 1){
    Console.WriteLine($"i = {i}");
    //i += 1; // Ее можно писать и здесь (2 вар) В шабке написать: (int i = 0; i < 5;)
}
Console.WriteLine("Цикл где переменная создается и удаляется закончился");

// Цикл с уже созданой переменной (без удаления)
int number = 0;

// Пробел с ; обязателен 
for ( ; ; number += 1){
    // Если в шапке то условие будет следующие: number < 5
    if (number >= 5){
        break;
    }
    Console.WriteLine($"number = {number}");
        
}
Console.WriteLine("Цикл с уже созданой переменной (без удаления) закончился");
Console.WriteLine($"number = {number}");

// Цикл с 2 переменными
for (int i = 0, j = 5 ; i < 5; i += 1, j -= 1){
    Console.WriteLine($"i = {i}, j = {j}");
}
Console.WriteLine("Цикл с 2 переменными закончился");
