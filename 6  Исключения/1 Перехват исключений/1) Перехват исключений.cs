



/*
Конструкция try - catch - finally
Данная конструкция позволяет отлавливать исключения в коде и делать какие
    либо действия если произошло исключение

try{
    "Код который хотим проверить на наличие исключений"
}
catch("исключение которое хотим поимать"){
    "Действие которое хотим сделать если нашли исключение"
}
finally{
    "Действие которое мы хотим сделать всегда"
}
*/


try{
    // Данное исключение сможет перехватить блок catch 1, catch 2
    throw new ArgumentException("Демонстрация возможностей try-catch (1, 2)"); 
    // Данное исключение сможет перехватить блок catch 2
    throw new Exception("Демонстрация возможностей try-catch (2)"); 
}
catch (ArgumentException ex){
    // ArgumentException - класс исключений который возникает 
    //    при передачи недопустимого аргумента, который передается методу.
    Console.WriteLine("Произошла ошибка. Были поданны не верные данные методу");
    Console.Write($"Подробности {ex.Message}");
}
catch (Exception error){
    // Exception - базовый класс исключений (он детектит все исключения)
    Console.WriteLine("Произошла неизвестная поломка программы");
    Console.WriteLine($"Подробности {error.Message}");
}
finally{
    Console.WriteLine("Данное сообщение будет выводится всегда");
}
